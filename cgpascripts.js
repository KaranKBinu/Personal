function generateAndDownloadPdf(
  selectedSemester,
  sgpa,
  percentage,
  selectedSubjects,
  customSubjects
) {
  // Create a new jsPDF instance
  var doc = new jsPDF();

  // Set the PDF name dynamically based on the selected semester
  var pdfName = `SGPA_${selectedSemester}_${Math.random()
    .toString(36)
    .substring(7)}.pdf`;

  // Set font size and text color
  var fontSize = 16;
  var textColor = "black";

  // Add content to the PDF
  doc.setFontSize(fontSize);
  doc.setTextColor(textColor);
  doc.text(
    `SGPA of ${selectedSemester}: ${sgpa.toFixed(2)}`,
    105,
    220,
    null,
    null,
    "center"
  );
  doc.text(
    `Percentage: ${percentage.toFixed(2)}%`,
    105,
    240,
    null,
    null,
    "center"
  );

  // Add a section for subjects and grades in a table format
  doc.text("Subjects and Grades:", 20, 40);

  // Set font size for the table
  fontSize = 12;
  doc.setFontSize(fontSize);

  // Define column widths
  var col1Width = 110; // Subject Name
  var col2Width = 30; // Subject Code
  var col3Width = 20; // Grade

  // Calculate column positions
  var col1X = 20;
  var col2X = col1X + col1Width;
  var col3X = col2X + col2Width;

  var rowY = 50;

  // Draw table header
  doc.rect(col1X, rowY, col1Width, 12); // Subject Name
  doc.text("Subject Name", col1X + 2, rowY + 8);
  doc.rect(col2X, rowY, col2Width, 12); // Subject Code
  doc.text("Subject Code", col2X + 2, rowY + 8);
  doc.rect(col3X, rowY, col3Width, 12); // Grade
  doc.text("Grade", col3X + 2, rowY + 8);

  // Draw table rows for selected subjects and custom subjects
  rowY += 12;
  selectedSubjects.forEach((subject) => {
    var subjectNameLines = doc.splitTextToSize(subject.name, col1Width - 5);
    var rowCount = subjectNameLines.length;
    var rowHeight = rowCount * fontSize;

    doc.rect(col1X, rowY, col1Width, rowHeight); // Subject Name
    doc.text(subjectNameLines, col1X + 4, rowY + 8);
    doc.rect(col2X, rowY, col2Width, rowHeight); // Subject Code
    doc.text(subject.code, col2X + 4, rowY + 8);
    doc.rect(col3X, rowY, col3Width, rowHeight); // Grade
    doc.text(subject.grade, col3X + 4, rowY + 8);

    rowY += rowHeight;
  });

  // Draw table rows for custom subjects if any
  if (customSubjects.length > 0) {
    customSubjects.forEach((customSubject) => {
      var customSubjectNameLines = doc.splitTextToSize(
        customSubject.name,
        col1Width - 5
      );
      var rowCount = customSubjectNameLines.length;
      var rowHeight = rowCount * fontSize;

      doc.rect(col1X, rowY, col1Width, rowHeight); // Custom Subject Name
      doc.text(customSubjectNameLines, col1X + 4, rowY + 8);
      doc.rect(col2X, rowY, col2Width, rowHeight); // Custom Subject Code
      doc.text(customSubject.code, col2X + 4, rowY + 8);
      doc.rect(col3X, rowY, col3Width, rowHeight); // Custom Grade
      doc.text(customSubject.grade, col3X + 4, rowY + 8);

      rowY += rowHeight;
    });
  }

  // Save the PDF with the dynamic name
  doc.save(pdfName);
}

