function calculateSGPA() {
  const selectedDepartment = document.getElementById("department").value;
  const selectedSemester = document.getElementById("semester").value;
  const courses = courseDetails[selectedDepartment][selectedSemester];

  let totalCredits = 0;
  let totalSubjectScore = 0;
  let invalidInput = false;

  const selectedSubjects = []; // An array to store selected subjects and grades

  courses.forEach((course, index) => {
    const creditInput = document.getElementById(`credit${index}`);
    const gradeInput = document.getElementById(`grade${index}`);
    const checkbox = document.getElementById(`subjectCheckbox${index}`);
    const creditCheckbox = document.getElementById(`creditCheckbox${index}`);

    const credit = parseFloat(creditInput.innerText);

    if (isNaN(credit) || credit < 0) {
      alert(
        `Invalid credit value for ${course.name} (${course.code}). Please enter a valid positive number.`
      );
      invalidInput = true;
      return;
    }

    const grade = gradeInput.value.toUpperCase();
    let gradeScore = 0;

    if (grade !== "F") {
      switch (grade) {
        case "S":
          gradeScore = 10;
          break;
        case "A":
          gradeScore = 9;
          break;
        case "B":
          gradeScore = 8;
          break;
        case "C":
          gradeScore = 7;
          break;
        case "D":
          gradeScore = 6;
          break;
        case "E":
          gradeScore = 5;
          break;
      }
    }

    if (creditCheckbox.checked) {
      totalCredits -= credit;
      checkbox.checked = true;
    }
    if (!checkbox.checked) {
      totalCredits += credit;
      totalSubjectScore += credit * gradeScore;

      // Store selected subject and grade in the array
      selectedSubjects.push({
        name: course.name,
        code: course.code,
        grade: grade,
      });
    } else {
      totalCredits += credit;
    }
  });

  // Calculate custom subjects' scores
  customSubjects.forEach((customSubject) => {
    totalCredits += customSubject.credits;
    const grade = customSubject.grade.toUpperCase();
    let gradeScore = 0;

    if (grade !== "F") {
      switch (grade) {
        case "S":
          gradeScore = 10;
          break;
        case "A":
          gradeScore = 9;
          break;
        case "B":
          gradeScore = 8;
          break;
        case "C":
          gradeScore = 7;
          break;
        case "D":
          gradeScore = 6;
          break;
        case "E":
          gradeScore = 5;
          break;
      }
    }

    totalSubjectScore += customSubject.credits * gradeScore;
  });

  if (invalidInput) {
    return;
  }

  const sgpa = totalSubjectScore / totalCredits;
  const percentage = sgpa * 9.5;

  document.getElementById("result").innerHTML = `
    <h2 class="text-center">SGPA of ${selectedSemester}: <span style="color: red;">${sgpa.toFixed(
    2
  )}</span></h2>
    <h2 class="text-center">Percentage: ${percentage.toFixed(2)}%</h2>
`;
  if (sgpa <= 10.0 || sgpa <= 10) {
    $("#resultModal").modal("show");
  } else {
    $("#resultModal").modal("hide");
  }

  document
    .getElementById("downloadPdfButton")
    .addEventListener("click", function () {
      // Call the function to generate and download the PDF with selected subjects and grades
      generateAndDownloadPdf(
        selectedSemester,
        sgpa,
        percentage,
        selectedSubjects,
        customSubjects
      );
    });
}

